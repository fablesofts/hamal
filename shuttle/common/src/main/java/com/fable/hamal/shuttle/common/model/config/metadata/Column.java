package com.fable.hamal.shuttle.common.model.config.metadata;

import java.io.Serializable;
import java.sql.Types;
/**
 * 
 * @author xieruidong 2013年11月4日 上午11:20:54
 */
public class Column implements Serializable {

	private static final long serialVersionUID = -2157306217539768262L;
	private String name;
	private Types type;
	private long size;
	private Long columnIndex;
	private String columnName;
	private String columnValue;
	private int columnType;
	private boolean isNull;
	private boolean isKey;
	
	public Column() {
		
	}
	
	public Column(String name) {
		this.name = name;
	}
	
	//---------------------------------setter/getter------------------------
	public String getName() {
		return name;
	}
	
	public void setName(String name) {
		this.name = name;
	}
	
	public Types getType() {
		return type;
	}
	
	public void setType(Types type) {
		this.type = type;
	}
	
	public long getSize() {
		return size;
	}
	
	public void setSize(long size) {
		this.size = size;
	}

	public Long getColumnIndex() {
		return columnIndex;
	}

	public void setColumnIndex(Long columnIndex) {
		this.columnIndex = columnIndex;
	}

	public String getColumnName() {
		return columnName;
	}

	public void setColumnName(String columnName) {
		this.columnName = columnName;
	}

	public String getColumnValue() {
		return columnValue;
	}

	public void setColumnValue(String columnValue) {
		this.columnValue = columnValue;
	}

	public int getColumnType() {
		return columnType;
	}

	public void setColumnType(int columnType) {
		this.columnType = columnType;
	}

	public boolean isNull() {
		return isNull;
	}

	public void setNull(boolean isNull) {
		this.isNull = isNull;
	}

	public boolean isKey() {
		return isKey;
	}

	public void setKey(boolean isKey) {
		this.isKey = isKey;
	}
	
}
