package com.fable.hamal.shuttle.common.data.media.source;

import java.util.Properties;

import com.fable.hamal.shuttle.common.data.media.DataCarrierSource;

/**
 * 
 * @author xieruidong 2013年11月12日 下午2:46:34
 */
public class DbCarrierSource extends DataCarrierSource {

	private static final long serialVersionUID = -6865410346223591142L;

	private String	url;
    private String	username;
    private String	password;
    private String	driver;
    private Properties	properties;
    
    
	public String getUrl() {
		return url;
	}
	
	public void setUrl(String url) {
		this.url = url;
	}
	
	public String getUsername() {
		return username;
	}
	
	public void setUsername(String username) {
		this.username = username;
	}
	
	public String getPassword() {
		return password;
	}
	
	public void setPassword(String password) {
		this.password = password;
	}
	
	public String getDriver() {
		return driver;
	}
	
	public void setDriver(String driver) {
		this.driver = driver;
	}
	
	public Properties getProperties() {
		return properties;
	}
	
	public void setProperties(Properties properties) {
		this.properties = properties;
	}
}
