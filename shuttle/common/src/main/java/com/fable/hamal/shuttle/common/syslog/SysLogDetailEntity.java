package com.fable.hamal.shuttle.common.syslog;

import java.io.Serializable;
import java.util.Date;


public class SysLogDetailEntity implements Serializable {

    private static final long serialVersionUID = -4957272577164813761L;

    private Long id;
    
    private Long sysLogId;
    
    private char operationType;
    
    private String operationDetail;
    
    private char operationResults;
    
    private Date startTime;
    
    private Date endTime;

    
    public Long getId() {
        return id;
    }

    
    public void setId(Long id) {
        this.id = id;
    }

    
    public Long getSysLogId() {
        return sysLogId;
    }

    
    public void setSysLogId(Long sysLogId) {
        this.sysLogId = sysLogId;
    }

    
    public char getOperationType() {
        return operationType;
    }

    
    public void setOperationType(char operationType) {
        this.operationType = operationType;
    }

    
    public String getOperationDetail() {
        return operationDetail;
    }

    
    public void setOperationDetail(String operationDetail) {
        this.operationDetail = operationDetail;
    }

    
    public char getOperationResults() {
        return operationResults;
    }

    
    public void setOperationResults(char operationResults) {
        this.operationResults = operationResults;
    }

    
    public Date getStartTime() {
        return startTime;
    }

    
    public void setStartTime(Date startTime) {
        this.startTime = startTime;
    }

    
    public Date getEndTime() {
        return endTime;
    }

    
    public void setEndTime(Date endTime) {
        this.endTime = endTime;
    }
    
    
    
}
