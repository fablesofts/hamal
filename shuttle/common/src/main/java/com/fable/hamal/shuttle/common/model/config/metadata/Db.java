package com.fable.hamal.shuttle.common.model.config.metadata;

import java.io.Serializable;

/**
 * 
 * @author xieruidong 2013年11月5日 上午11:05:26
 */
public class Db implements Serializable {
	
	private static final long serialVersionUID = 6096608392092251878L;
	private long id;
	private String username;
	private String password;
	private String serverIp;
	private String dbType;//"mysql,oracle,mssql,dameng"
	private String dbName;
	private String connectUrl;

	
	public long getId() {
		return id;
	}

	public void setId(long id) {
		this.id = id;
	}
	
	public String getUsername() {
		return username;
	}

	public void setUsername(String username) {
		this.username = username;
	}

	public String getPassword() {
		return password;
	}

	public void setPassword(String password) {
		this.password = password;
	}

	public String getServerIp() {
		return serverIp;
	}

	public void setServerIp(String serverIp) {
		this.serverIp = serverIp;
	}

	public String getDbType() {
		return dbType;
	}

	public void setDbType(String dbType) {
		this.dbType = dbType;
	}

	public String getDbName() {
		return dbName;
	}

	public void setDbName(String dbName) {
		this.dbName = dbName;
	}

	public String getConnectUrl() {
		return connectUrl;
	}
	
	public void setConnectUrl(String connectUrl) {
		this.connectUrl = connectUrl;
	}
}
