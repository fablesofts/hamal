package com.fable.hamal.shuttle.common.model.envelope.et.converter;

import java.io.Serializable;
import java.util.List;

/**
 * 
 * @author xieruidong 2013年11月5日 上午10:53:10
 */
public class FileContentConverter implements Serializable {

	private static final long serialVersionUID = 4736871658883814554L;
	
	private String name;
    private List<String> content;
    private int type;
    
    public String getName() {
        return name;
    }
    
    public void setName(String name) {
        this.name = name;
    }
    
    public List<String> getContent() {
        return content;
    }
    
    public void setContent(List<String> content) {
        this.content = content;
    }
    
    public int getType() {
        return type;
    }
    
    public void setType(int type) {
        this.type = type;
    }
    
    
	
}
