package com.fable.hamal.log.model;

import java.io.Serializable;


public class RowLevelLog implements Serializable {

    private static final long serialVersionUID = 8135541874390775865L;

    private Long id;
    
    private Long sysLogDetailId;
    
    private String operationData;
    
    private char operationResults;

    
    public Long getId() {
        return id;
    }

    
    public void setId(Long id) {
        this.id = id;
    }

    
    public Long getSysLogDetailId() {
        return sysLogDetailId;
    }

    
    public void setSysLogDetailId(Long sysLogDetailId) {
        this.sysLogDetailId = sysLogDetailId;
    }

    
    public String getOperationData() {
        return operationData;
    }

    
    public void setOperationData(String operationData) {
        this.operationData = operationData;
    }

    
    public char getOperationResults() {
        return operationResults;
    }

    
    public void setOperationResults(char operationResults) {
        this.operationResults = operationResults;
    }
    
    
    
    
}
