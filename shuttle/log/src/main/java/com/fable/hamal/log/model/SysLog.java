package com.fable.hamal.log.model;

import java.io.Serializable;
import java.sql.Timestamp;


public class SysLog implements Serializable {

    private static final long serialVersionUID = 2771467111268524282L;

    private Long id;
    
    private Long taskId;
    
    private String taskSerial;
    
    private String taskName;
    
    private Integer selectCount;
    
    private Integer selectData;
    
    private Integer loadCount;
    
    private Integer loadData; 
    
    /**默认为成功*/
    private String operationResults = "0";
    
    private Timestamp startTime;
    
    private Timestamp endTime;

    
    public Long getId() {
        return id;
    }

    
    public void setId(Long id) {
        this.id = id;
    }

    
    public Long getTaskId() {
        return taskId;
    }

    
    public void setTaskId(Long taskId) {
        this.taskId = taskId;
    }

    
    public String getTaskName() {
        return taskName;
    }

    
    public void setTaskName(String taskName) {
        this.taskName = taskName;
    }

    
    public Integer getSelectCount() {
        return selectCount;
    }

    
    public void setSelectCount(Integer selectCount) {
        this.selectCount = selectCount;
    }

    
    public Integer getSelectData() {
        return selectData;
    }

    
    public void setSelectData(Integer selectData) {
        this.selectData = selectData;
    }

    
    public Integer getLoadCount() {
        return loadCount;
    }

    
    public void setLoadCount(Integer loadCount) {
        this.loadCount = loadCount;
    }

    
    public Integer getLoadData() {
        return loadData;
    }

    
    public void setLoadData(Integer loadData) {
        this.loadData = loadData;
    }

    
    public String getOperationResults() {
        return operationResults;
    }

    
    public void setOperationResults(String operationResults) {
        this.operationResults = operationResults;
    }

    
    public Timestamp getStartTime() {
        return startTime;
    }

    
    public void setStartTime(Timestamp startTime) {
        this.startTime = startTime;
    }

    
    public Timestamp getEndTime() {
        return endTime;
    }

    
    public void setEndTime(Timestamp endTime) {
        this.endTime = endTime;
    }


	public String getTaskSerial() {
		return taskSerial;
	}


	public void setTaskSerial(String taskSerial) {
		this.taskSerial = taskSerial;
	}
}
