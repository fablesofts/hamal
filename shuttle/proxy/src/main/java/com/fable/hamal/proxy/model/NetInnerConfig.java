public class NetInnerConfig {

	private Long id;
	private String hostname;
	private String toGapIp;
	private String toInnerNetIp;
	private Integer innerProxyPort;
	private Integer outerProxyPort;
	private Integer serviceInPort;
	private Integer serviceOutPort;
	
	public Long getId() {
		return id;
	}
	public void setId(Long id) {
		this.id = id;
	}
	public String getHostname() {
		return hostname;
	}
	public void setHostname(String hostname) {
		this.hostname = hostname;
	}
	public String getToGapIp() {
		return toGapIp;
	}
	public void setToGapIp(String toGapIp) {
		this.toGapIp = toGapIp;
	}
	public String getToInnerNetIp() {
		return toInnerNetIp;
	}
	public void setToInnerNetIp(String toInnerNetIp) {
		this.toInnerNetIp = toInnerNetIp;
	}
	public Integer getInnerProxyPort() {
		return innerProxyPort;
	}
	public void setInnerProxyPort(Integer innerProxyPort) {
		this.innerProxyPort = innerProxyPort;
	}
	public Integer getOuterProxyPort() {
		return outerProxyPort;
	}
	public void setOuterProxyPort(Integer outerProxyPort) {
		this.outerProxyPort = outerProxyPort;
	}
	public Integer getServiceInPort() {
		return serviceInPort;
	}
	public void setServiceInPort(Integer serviceInPort) {
		this.serviceInPort = serviceInPort;
	}
	public Integer getServiceOutPort() {
		return serviceOutPort;
	}
	public void setServiceOutPort(Integer serviceOutPort) {
		this.serviceOutPort = serviceOutPort;
	}
}
