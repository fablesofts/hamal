package com.fable.hamal.ftp.client;

/**
 * 
 * @author xieruidong 2014年5月16日 下午3:28:32
 */
public class FTPConnectionParameter {

	/**ftp服务器端ip地址*/
	private String host;
	/**ftp服务器端端口号*/
	private int port;
	/**ftp用户名*/
	private String username;
	/**ftp用户密码*/
	private String password;
	
	public FTPConnectionParameter() {
		
	}
	
	public FTPConnectionParameter(String host, int port, String username, String password) {
		this.host = host;
		this.port = port;
		this.username = username;
		this.password = password;
	}
	
	public String getHost() {
		return host;
	}
	
	public void setHost(String host) {
		this.host = host;
	}
	public int getPort() {
		return port;
	}
	public void setPort(int port) {
		this.port = port;
	}
	
	public String getUsername() {
		return username;
	}
	
	public void setUsername(String username) {
		this.username = username;
	}
	
	public String getPassword() {
		return password;
	}
	
	public void setPassword(String password) {
		this.password = password;
	}
}
